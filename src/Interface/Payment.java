    public Payment(UndergradStu u) {
        initComponents();
        
        sid = u.getStudentID();
        idLabel.setText("Student ID: "+Integer.toString(sid));
        
        con = ConnectDB.connect();
        
        try {
            int id = u.getStudentID();
            String sql = "SELECT ustu_subject.subjectId AS 'Subject ID', subject.name AS 'Subject Name', subject.price AS 'Price' FROM ustu_subject JOIN subject ON ustu_subject.subjectId = subject.subjectId WHERE ustu_subject.studentId = '"+ id +"'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            subjectsTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null,e);
        }
        
        try {
            int id = u.getStudentID();
            String sql = "SELECT SUM(subject.price) AS 'Price' FROM ustu_subject JOIN subject ON ustu_subject.subjectId = subject.subjectId WHERE ustu_subject.studentId = '"+ id +"'";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            String total = "";
            while(rs.next()) {
                total = rs.getString("Price");
            }
            
            
            totalLabel.setText("Rs: "+total);
            
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        
    }
    
