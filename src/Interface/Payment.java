    private void payButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payButtonActionPerformed
        
        double amount = Double.parseDouble(amountBox.getText());
        int id = sid;
        String payedDate = java.time.LocalDate.now().toString();
        
        if(amount < 12000) {
            JOptionPane.showConfirmDialog(null, "Please make a payment higher than Rs:12000", "Payment Not Sufficient", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            
            con = ConnectDB.connect();
            
            try {
                
                String sql = "INSERT INTO payment (studentId, payedAmount, payedDay) VALUES ('"+ id +"', '"+ amount +"', '"+ payedDate +"')";
                pst = con.prepareStatement(sql);
                pst.execute();
            }
            catch (Exception e) {
                JOptionPane.showConfirmDialog(null, e);
            }
            
            JOptionPane.showConfirmDialog(null, "Payment Successfull", "Payment Done", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            StudentEnrollment se = new StudentEnrollment();
            se.setVisible(true);
        }
        
    }//GEN-LAST:event_payButtonActionPerformed

