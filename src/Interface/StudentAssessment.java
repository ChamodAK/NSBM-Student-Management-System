    private void atUpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atUpdateButtonActionPerformed
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to update this record?");
        
        String assId = atAssId2Box.getText();
        String assType = atAssType2Box.getText();
        String subId = atSubId2Box.getText();
        
        if(x==0) {
        
            AssessmentType at = new AssessmentType(assId, assType, subId);
            at.update();
                
            tableLoadat();

            atAssId2Box.setText("");
            atAssType2Box.setText("");
            atSubId2Box.setText("");
                
        }
        
    }//GEN-LAST:event_atUpdateButtonActionPerformed

    private void atDeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atDeleteButtonActionPerformed
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to delete this record?");
        
        if(x==0) {
        
            String assId = atAssId2Box.getText();
            AssessmentType at = new AssessmentType(assId);
            at.delete();
                
            tableLoadat();

            atAssId2Box.setText("");
            atAssType2Box.setText("");
            atSubId2Box.setText("");
                
        }
        
    }//GEN-LAST:event_atDeleteButtonActionPerformed

