    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        
        if(studentIDBox.getText().isEmpty() || assessmentIDBox.getText().isEmpty() || marksBox.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Please Fill All Fields", "Fields are Empty!", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            
            String sid = studentIDBox.getText();
            String assid = assessmentIDBox.getText();
            int marks = Integer.parseInt(marksBox.getText());
            
            StuAssessment sa = new StuAssessment(sid, assid, marks);
            sa.addStuAssess();
            
            studentIDBox.setText("");
            assessmentIDBox.setText("");
            marksBox.setText("");
            tableLoad();
            
        }
        
    }//GEN-LAST:event_submitButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to delete the record?");
        
        if(x==0) {
        
            String sid = sidBox.getText();
            StuAssessment sa = new StuAssessment(sid);
            sa.deleteStuAssess();
            
            
            tableLoad();

            sidBox.setText("");
            assBox.setText("");
            markBox.setText("");
        
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

