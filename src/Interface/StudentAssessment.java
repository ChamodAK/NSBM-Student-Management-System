    // load stu_assess table
    public void tableLoad(){
        
        try {
            
            String sql = "SELECT studentId AS 'Student ID', assessmentId AS 'Assessment ID', marks AS 'Marks' FROM stu_assess";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            assessmentTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        
    }
    
    // load assessment table
    public void tableLoadat(){
        
        try {
            
            String sql = "SELECT id AS 'Assessment ID', type AS 'Assessment Type', subjectId AS 'Subject ID' FROM assessment";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            atTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        
    }

