    private void updateLabButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateLabButtonActionPerformed
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to update this record?");
        
        String labId = labIdBox.getText();
        String labName = labNameBox.getText();
        int capacity = Integer.parseInt(labCapacityBox.getText());
        
        if(x==0) {
        
            Lab l = new Lab(labId, labName, capacity);
            l.updateLab();
       
            tableLoadLab();
                
            labIdBox.setText("");
            labNameBox.setText("");
            labCapacityBox.setText("");
                
        }
        
    }//GEN-LAST:event_updateLabButtonActionPerformed

    private void addLabButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLabButtonActionPerformed
        
        if(labIdBox.getText().isEmpty() || labNameBox.getText().isEmpty() || labCapacityBox.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Please Fill All Fields", "Fields are Empty!", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            
            String labId = labIdBox.getText();
            String labName = labNameBox.getText();
            int capacity = Integer.parseInt(labCapacityBox.getText());
            
            Lab l = new Lab(labId, labName, capacity);
            
            l.addLab();
            
            labIdBox.setText("");
            labNameBox.setText("");
            labCapacityBox.setText("");
            tableLoadLab();
            
        }
        
    }//GEN-LAST:event_addLabButtonActionPerformed

    private void labTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labTableMouseClicked
        
        int r = labTable.getSelectedRow();
        
        String labId = labTable.getValueAt(r, 0).toString();
        String labName = labTable.getValueAt(r, 1).toString();
        String capacity = labTable.getValueAt(r, 2).toString();
        
        labIdBox.setText(labId);
        labNameBox.setText(labName);
        labCapacityBox.setText(capacity);
        
    }//GEN-LAST:event_labTableMouseClicked

