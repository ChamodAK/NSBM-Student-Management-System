    //importing Lab table from database
    public void tableLoadLab(){
        
        try {
            
            String sql = "SELECT labId AS 'Lab ID', name AS 'Lab Name', capacity AS 'Lab Capacity' FROM lab";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            labTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        
    }
    
    //importing lab_instruct table from database
    public void tableLoadLA(){
        
        try {
            
            String sql = "SELECT labId AS 'Lab ID', instructId AS 'Instructor ID' FROM lab_instruct";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            
            assignLabTable.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch (Exception e) {
            JOptionPane.showConfirmDialog(null, e);
        }
        
    }

