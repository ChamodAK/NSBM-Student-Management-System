    private void seAddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seAddBtnActionPerformed
        
        if(seSid.getText().isEmpty() || seSubId.getText().isEmpty()) {
            
            JOptionPane.showConfirmDialog(null, "Please Fill All Fields", "Fields are Empty!", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            
        }
        
        else {
            
            String sid = seSid.getText();
            String subId = seSubId.getText();
            
            StudentSubjects ss = new StudentSubjects(sid, subId);
            ss.add();
            
            seSid.setText("");
            seSubId.setText("");
            
            tableLoadUndergradSub();
            
        }
        
    }//GEN-LAST:event_seAddBtnActionPerformed

    private void seDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seDeleteBtnActionPerformed
        
        int x = JOptionPane.showConfirmDialog(null, "Do you want to delete this record?");
        
        if(x==0) {
        
            String sId = seSid.getText();
            String subId = seSubId.getText();
            
            StudentSubjects ss = new StudentSubjects(sId, subId);
            ss.delete();
           
            tableLoadUndergradSub();

            seSid.setText("");
            seSubId.setText("");
        
        }
        
    }//GEN-LAST:event_seDeleteBtnActionPerformed

