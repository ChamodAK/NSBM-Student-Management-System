    private void au2enrollButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_au2enrollButtonActionPerformed
        
        if(courseComboBox.getSelectedIndex()==0) {
            JOptionPane.showConfirmDialog(null, "Please Select a Intake & a Course", "Not Selected", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
       
        int sem1count = 0;
        int sem2count = 0;
        int intakeIndex = intakeComboBox.getSelectedIndex();
       
        JCheckBox[] checkBoxes = {sem1sub1, sem1sub2, sem1sub3, sem1sub4, sem1sub5, sem2sub1, sem2sub2, sem2sub3, sem2sub4, sem2sub5};
       
      
        for(int i=0; i<5; i++) {
            if(checkBoxes[i].isSelected()) {
                sem1count += 1;
            }
        }
       
        for(int i=5; i<10; i++) {
            if(checkBoxes[i].isSelected()) {
                sem2count += 1;
            }
        }
        
        if(sem1count != 4 && intakeIndex==1) {
            JOptionPane.showConfirmDialog(null, "Please Select 4 Subject for Semester 1", "Invalid Number of Subjects!", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        else if(sem2count != 4) {
            JOptionPane.showConfirmDialog(null, "Please Select 4 Subject for Semester 2", "Invalid Number of Subjects!", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
        
        else {
          
            String name = nameBox.getText();
            String dob = dobBox.getText();
            char gender = genderButtonGroup.getSelection().getActionCommand().charAt(0);
            String nic = nicBox.getText();
            String address = addressBox.getText();
            String phone = phoneBox.getText();
            String email = emailBox.getText();
            
            singleRadioButton.setActionCommand("Single");
            marriedRadioButton.setActionCommand("Married");
            String status = civilButtonGroup.getSelection().getActionCommand();
            
            String result = alSub1Box.getText()+", "+alSub2Box.getText()+", "+alSub3Box.getText();
            int rank = Integer.parseInt(alRankBox.getText());
            
            String course = (String) courseComboBox.getSelectedItem();
            String intake = (String) intakeComboBox.getSelectedItem();
            
            String sem1[] = new String[4];
            String sem2[] = new String[4];
            int sem1Int = 0;
            int sem2Int = 0;
            
            for(int i=0; i<5; i++) {
                if(checkBoxes[i].isSelected()) {
                    sem1[sem1Int] = checkBoxes[i].getActionCommand();
                    sem1Int += 1;
                }
            }
       
            for(int i=5; i<10; i++) {
                if(checkBoxes[i].isSelected()) {
                    sem2[sem2Int] = checkBoxes[i].getActionCommand();
                    sem2Int += 1;
                }
            }
            
            
            UndergradStu us = new UndergradStu(name, dob, gender, nic, status, phone, email, address, result, rank);
            
            if(intakeIndex==1) {
                us.enroll(course, sem1, sem2, intake);
            }
            else {
                us.enroll(course, sem2, intake);
            }
            
            Payment p = new Payment(us);
            p.setVisible(true);
            this.setVisible(false);
            
        }
        
    }//GEN-LAST:event_au2enrollButtonActionPerformed

